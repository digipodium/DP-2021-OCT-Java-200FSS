public class Conditions {
    public static void main(String[] args) {
        int num = 11;

        if(num%2==0){
            System.out.println("even");
        }else{
            System.out.println("odd");
        }

        int marks = 78;

        if(marks >= 90){
            System.out.println("Grade A");
        }else if(marks >= 70){
            System.out.println("Grade B");
        }

    }
}
